package xjtu.model;

public class Activity {
	
	private int id;
	private String activityName;
	private int problemId;
	private String envent;
	private String activityProblem;
	private int projectId;
	private int creatorId;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getActivityName() {
		return activityName;
	}
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	public int getProblemId() {
		return problemId;
	}
	public void setProblemId(int problemId) {
		this.problemId = problemId;
	}
	public String getEnvent() {
		return envent;
	}
	public void setEnvent(String envent) {
		this.envent = envent;
	}
	public String getActivityProblem() {
		return activityProblem;
	}
	public void setActivityProblem(String activityProblem) {
		this.activityProblem = activityProblem;
	}
	public int getProjectId() {
		return projectId;
	}
	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}
	public int getCreatorId() {
		return creatorId;
	}
	public void setCreatorId(int creatorId) {
		this.creatorId = creatorId;
	}
	
	
	
	
	
}
